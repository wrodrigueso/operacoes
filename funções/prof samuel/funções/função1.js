// Verificando se o número é positivo
function VerificarNumero (num) {
    if (num > 0) {
        console.log("O número é positivo");
    } else if (num < 0) {
        console.log("O número é negativo");
    } else { (num = 0)
        console.log("O número é zero");
    }
}

VerificarNumero(10);
VerificarNumero(-5);