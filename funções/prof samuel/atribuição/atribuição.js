// atribuição simples (=)
let numero = 10;
console.log("Atribuição simples: numero =", numero);

// atribuição de adição (+=)
numero += 5;
console.log("Após atribuição de adição (+=5): numero=", numero);

// atribuição de subtração (-=)
numero -= 3;
console.log("Após atribuição de subtração (-=): numero=", numero);

// atribuição de multiplicação (*=)
numero *= 2;
console.log("Após atribuição de multiplicação (*=): numero=", numero);

// atribuição de divisão (/=)
numero /= 4;
console.log("Após atribuição de divisão (/=): numero=", numero);